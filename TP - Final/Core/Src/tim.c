void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim)
{
  /* TIM1 - Heartbeat */ 
  if (htim->Instance == TIM1){
    static uint32_t timeLED;
    timeLED++;

    //LED first beat start
    if (timeLED == 1000){ //turn on
      HAL_GPIO_WritePin(GPIOC, ONBOARD_LED_Pin, GPIO_PIN_RESET);
    }
    //LED fist beat end
    if (timeLED == 1100){ //turn off
      HAL_GPIO_WritePin(GPIOC, ONBOARD_LED_Pin, GPIO_PIN_SET);
    }
    //LED first beat start
    if (timeLED == 1400){ //turn on
      HAL_GPIO_WritePin(GPIOC, ONBOARD_LED_Pin, GPIO_PIN_RESET);
    }
    //LED fist beat end
    if (timeLED == 1500){ //turn off
      HAL_GPIO_WritePin(GPIOC, ONBOARD_LED_Pin, GPIO_PIN_SET);
      timeLED = 0;
    }

  }
  /* TIM3 */ 
  else if(htim->Instance == TIM3){
    if(Motor1 == START){
      HAL_GPIO_TogglePin(MOTOR1_Port, MOTOR1_Pin);
      stepsM1--;
      if(stepsM1==0){
        Motor1 = STOP;
        motorBUSY = false;
      }
    }
    else if(Motor2 == START){
      HAL_GPIO_TogglePin(MOTOR2_Port, MOTOR2_Pin);
      stepsM2--;
      if(stepsM2==0){
        Motor2 = STOP;
        motorBUSY = false;
      }
    }
    else if(Motor3 == START){
      HAL_GPIO_TogglePin(MOTOR3_Port, MOTOR3_Pin);
      stepsM3--;
      if(stepsM3==0){
        Motor3 = STOP;
        motorBUSY = false;
      }
    }

  }
}